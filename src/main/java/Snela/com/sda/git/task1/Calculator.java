package Snela.com.sda.git.task1;

public class Calculator {
        public float plus(float a, float b) {
            return a + b;
        }
        public float minus(float a, float b) {
            return a - b;
        }
        public float times(float a, float b) {
            return a * b;
        }
        public float by(float a, float b) {
            return a / b;
        }
}
